#define LABELINDEXSIZE (sizeof(LabelIndex)/sizeof(u16))
#define LABELTEXTSIZE (sizeof(LabelText)/sizeof(u8))
#define CTRLCOLORSIZE (sizeof(ctrlcolor)/sizeof(ST_CtrlColor))


const ST_CtrlColor* GetShareColor(u16 index)
{
	if(index>=CTRLCOLORSIZE)
		return ctrlcolor+(CTRLCOLORSIZE-1);
	return ctrlcolor+index;
}

const ST_LabelFont* GetShareLabelFont(u16 index)
{
	
	if(FORE_GetIO(PIPE_LANGRAGE))
	{
		if(index>=LABELFONTSIZE)
			return labelfont_E+(LABELFONTSIZE-1);
		return labelfont_E+index;
	}
	if(index>=LABELFONTSIZE)
		return labelfont_C+(LABELFONTSIZE-1);
	return labelfont_C+index;
}

const u8* GetShareString(u16 index)
{
	u8 langrage;
	if(FORE_GetIO(PIPE_LANGRAGE))
		langrage = 1;
	else
		langrage = 0;

	//语言选择更改
	if(langrage>=STRING_STYLE)
		return pLabelText[0];		//返回空

	if(index>=STRING_COUNT)
		return pLabelText[langrage];		//返回空	
	else
	{
		index = (pLabelIndex[langrage])[index];
		return pLabelText[langrage]+index;
	}

}
